package com.chang.once;


public class AA {

    private Long aa;

    private String bb;

    private Integer cc;

    private AA self;

    public Long getAa() {
        return aa;
    }

    public void setAa(Long aa) {
        this.aa = aa;
    }

    public String getBb() {
        return bb;
    }

    public void setBb(String bb) {
        this.bb = bb;
    }

    public Integer getCc() {
        return cc;
    }

    public void setCc(Integer cc) {
        this.cc = cc;
    }

    public AA getSelf() {
        return self;
    }

    public void setSelf(AA self) {
        this.self = self;
    }

}
